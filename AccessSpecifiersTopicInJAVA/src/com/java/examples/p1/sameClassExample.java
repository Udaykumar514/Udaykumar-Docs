package com.java.examples.p1;

public class sameClassExample {

	private int a = 10;
	int b = 20;
	protected int c = 30;
	public int d = 40;
	
	public void sameClass() {
		
		System.out.println("A value From same class :" +a);
		System.out.println("B value From same class :" +b);
		System.out.println("c value From same class :" +c);
		System.out.println("D value From same class :" +d);
	}
		
}
